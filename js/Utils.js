export function gradosARadianes ( p_grados ) {
	let radianes = p_grados * Math.PI / 180;
	
	return radianes;
}

/* ************************************************************************************************************ */

export function radianesAGrados ( p_radianes ) {
	let grados = p_radianes * 180 / Math.PI;
	
	return grados;
}

